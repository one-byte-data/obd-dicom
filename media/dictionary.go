package media

// DictStruct Dictionary Structure definition
type DictStruct struct {
	group   uint16
	element uint16
	vr      string
	text    string
}

var codes []DictStruct

//AddVRData get info from Dictionary
func AddVRData(group uint16, element uint16) string {
	if codes == nil {
		return "ERROR"
	}
	for i := 0; i < len(codes); i++ {
		if (group == codes[i].group) && (element == codes[i].element) {
			return codes[i].vr
		}
	}
	return "UN"
}

//TagDescription get info from Dictionary
func TagDescription(group uint16, element uint16) string {
	if codes == nil {
		return "ERROR"
	}
	for i := 0; i < len(codes); i++ {
		if (group == codes[i].group) && (element == codes[i].element) {
			return codes[i].text
		}
	}
	return ""
}

